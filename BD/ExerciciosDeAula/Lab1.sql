--Ex 1
CREATE TABLE TIME(
    NOME VARCHAR(30) NOT NULL,
    ESTADO CHAR(2),
    TIPO VARCHAR2(12),
    SALDO_GOLS NUMBER(4,0),
    
    CONSTRAINT PK_TIME PRIMARY KEY(NOME),
    
    CONSTRAINT CK_TIME_TAMANHO_ESTADO CHECK (ESTADO IN (
        'AC', 'AL', 'AP', 'AM', 'BA', 'CE', 'DF', 'ES', 'GO', 
        'MA', 'MT', 'MS', 'MG', 'PA', 'PB', 'PR', 'PE', 'PI', 
        'RJ', 'RN', 'RS', 'RO', 'RR', 'SC', 'SP', 'SE', 'TO'
    )),
    CONSTRAINT CK_TIME_TIPO CHECK(TIPO IN ('AMADOR', 'PROFISSIONAL'))
);

CREATE TABLE JOGA(
    TIME1 VARCHAR2(30) NOT NULL,
    TIME2 VARCHAR2(30) NOT NULL,
    CLASSICO CHAR(1),
    
    CONSTRAINT PK_JOGA PRIMARY KEY(TIME1, TIME2),
    CONSTRAINT FK_JOGA_TIME1 FOREIGN KEY(TIME1) REFERENCES TIME(NOME) ON DELETE CASCADE,
    CONSTRAINT FK_JOGA_TIME2 FOREIGN KEY(TIME2) REFERENCES TIME(NOME) ON DELETE CASCADE,
    
    CONSTRAINT CK_JOGA_TIMES_DIFERENTES CHECK(TIME1 != TIME2),
    CONSTRAINT CK_JOGA_CLASSICO CHECK(CLASSICO IN ('S', 'N'))
);

CREATE TABLE PARTIDA(
    TIME1 VARCHAR2(30) NOT NULL,
    TIME2 VARCHAR2(30) NOT NULL,
    DATA DATE NOT NULL,
    PLACAR VARCHAR2(5) DEFAULT '0X0',
    LOCAL VARCHAR2(30),
    
    CONSTRAINT PK_PARTIDA PRIMARY KEY(TIME1, TIME2, DATA),
    CONSTRAINT FK_PARTIDA_TIME1_TIME2 FOREIGN KEY(TIME1, TIME2) REFERENCES JOGA(TIME1, TIME2) ON DELETE CASCADE,
    
    CONSTRAINT CK_PARTIDA_PLACAR CHECK(REGEXP_LIKE(PLACAR, '^([0-9]{1,2})X([0-9]{1,2})$'))
);

CREATE TABLE JOGADOR(
    CPF CHAR(14) NOT NULL,
    RG CHAR(12) NOT NULL,
    NOME VARCHAR2(30) NOT NULL,
    DATA_NASC DATE,
    NATURALIDADE VARCHAR2(15),
    TIME VARCHAR2(30) NOT NULL,
    
    CONSTRAINT PK_JOGADOR PRIMARY KEY(CPF),
    CONSTRAINT SK_JOGADOR UNIQUE(RG, NOME),
    CONSTRAINT FK_JOGADOR_TIME FOREIGN KEY(TIME) REFERENCES TIME(NOME) ON DELETE CASCADE,
    
    CONSTRAINT CK_JOGADOR_CPF CHECK(REGEXP_LIKE(CPF, '[0-9]{3}\.[0-9]{3}\.[0-9]{3}\-[0-9]{2}')),
    CONSTRAINT CK_JOGADOR_RG CHECK(REGEXP_LIKE(RG, '[0-9]{2}\.[0-9]{3}\.[0-9]{3}\-[0-9]'))
);

CREATE TABLE POSICAO_JOGADOR(
    JOGADOR CHAR(14) NOT NULL,
    POSICAO VARCHAR2(15) NOT NULL,
    
    CONSTRAINT PK_POSICAO_JOGADOR PRIMARY KEY(JOGADOR, POSICAO),
    CONSTRAINT FK_POSICAO_JOGADOR_JOGADOR FOREIGN KEY(JOGADOR) REFERENCES JOGADOR(CPF) ON DELETE CASCADE
);

CREATE TABLE DIRETOR(
    TIME VARCHAR2(30) NOT NULL,
    NOME VARCHAR2(30) NOT NULL,
    
    CONSTRAINT PK_DIRETOR PRIMARY KEY(TIME, NOME),
    CONSTRAINT FK_DIRETOR_TIME FOREIGN KEY(TIME) REFERENCES TIME(NOME) ON DELETE CASCADE
);

CREATE TABLE UNIFORME(
    TIME VARCHAR2(30) NOT NULL,
    TIPO CHAR(7) NOT NULL,
    COR_PRINCIPAL VARCHAR2(15),
    
    CONSTRAINT PK_UNIFORME PRIMARY KEY(TIME, TIPO),
    CONSTRAINT FK_UNIFORME_TIME FOREIGN KEY(TIME) REFERENCES TIME(NOME) ON DELETE CASCADE,
    
    CONSTRAINT CK_UNIFORME_TIPO CHECK(TIPO IN ('TITULAR', 'RESERVA'))
);

